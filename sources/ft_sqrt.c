#include "libft.h"

int	ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb <= 0)
		return (i);
	if (nb > 0)
	{
		while (i * i <= nb)
		{
			i++;
			if (i * i == nb)
				return (i);
		}
	}
	return (i);
}
